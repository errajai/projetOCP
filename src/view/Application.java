    private void updateAEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAEActionPerformed
       lMessage.setText("");
       DefaultTableModel model = (DefaultTableModel) jTableAE.getModel();
       if(updateAE.getText() == "Modifier"){
       if(jTableAE.getSelectedRow() == -1){
           if(jTableAE.getRowCount() == 0)
                lMessage.setText("Le tableau est vide .");
           else
                lMessage.setText("Vous devez séléctionner une ligne .");
                        
       }else{
           addAE.setEnabled(false);
           deleteAE.setEnabled(false);
           EffaceAE.setEnabled(false);
           updateAE.setText("Confirmer");
           cancelUpdate.setVisible(true);
           txtNAE.setText(model.getValueAt(jTableAE.getSelectedRow(),0).toString());
           txtMontantAE.setText(model.getValueAt(jTableAE.getSelectedRow(),1).toString());
           txtDate.setText(model.getValueAt(jTableAE.getSelectedRow(),2).toString());
           txtCredit.setText(model.getValueAt(jTableAE.getSelectedRow(),3).toString());
           txtNAE.setEnabled(false);
       }
       }else if(!txtNAE.getText().trim().isEmpty() && !txtCredit.getText().trim().isEmpty() && !txtMontantAE.getText().trim().isEmpty() && !txtDate.getText().trim().isEmpty() ){
            
                try {
                    controllerAE.updateAE(txtNAE.getText(), txtMontantAE.getText(), txtDate.getText(),txtCredit.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(Application.class.getName()).log(Level.SEVERE, null, ex);
                }
                clearAE();
                updateAE.setText("Modifier");
                cancelUpdate.setVisible(false);
                try {
                    loadAE();
                    txtNAE.setEnabled(true);
                    addAE.setEnabled(true);
                    deleteAE.setEnabled(true);
                    EffaceAE.setEnabled(true);
                } catch (SQLException | ParseException ex) {
                    Logger.getLogger(Application.class.getName()).log(Level.SEVERE, null, ex);
                }
        }
        else{
            lMessage.setText("Veuillez remplir tous les champs .");
        }
       
       
       
    }//GEN-LAST:event_updateAEActionPerformed

