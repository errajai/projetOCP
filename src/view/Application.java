    private void deleteAEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAEActionPerformed
        lMessage.setText("");
        DefaultTableModel model = (DefaultTableModel) jTableAE.getModel();
        if(jTableAE.getSelectedRow() == -1){
           if(jTableAE.getRowCount() == 0)
                lMessage.setText("Le tableau est vide .");
           else
                lMessage.setText("Vous devez séléctionner une ligne .");
        }else{
            try {
                controllerAE.deleteAE(model.getValueAt(jTableAE.getSelectedRow(),0).toString());
                loadAE();
            } catch (SQLException | ParseException ex) {
            Logger.getLogger(Application.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
    }//GEN-LAST:event_deleteAEActionPerformed
    
    public void loadAE () throws SQLException, ParseException{
        DefaultTableModel model = (DefaultTableModel) jTableAE.getModel();
        model.getDataVector().removeAllElements();
        ResultSet rs = controllerAE.getAllAE();
        while(rs.next()){
            
            SimpleDateFormat fromFormat = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat toFormat = new SimpleDateFormat("dd/MM/yyyy"); // convert to dd/MM/yyyy format
            DecimalFormat df = new DecimalFormat("########.00"); // convert float to .00
            java.util.Date date = fromFormat.parse(rs.getDate("Date").toString());
            model.addRow(new Object[]{rs.getInt("NAE"),df.format(rs.getFloat("MontantAE")),toFormat.format(date),rs.getInt("NCredit")});
        }
    }
