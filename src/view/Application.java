    private void addAEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAEActionPerformed
        
        if(!txtNAE.getText().trim().isEmpty() && !txtCredit.getText().trim().isEmpty() && !txtMontantAE.getText().trim().isEmpty() && !txtDate.getText().trim().isEmpty() ){
            if(!controllerAE.isInAE(Integer.parseInt(txtNAE.getText().trim()))){
            controllerAE.addAE(txtNAE.getText(), txtMontantAE.getText(), txtDate.getText(),txtCredit.getText());
            clearAE();
                try {
                    loadAE();
                } catch (SQLException | ParseException ex) {
                    Logger.getLogger(Application.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else
                lMessage.setText("N° AE : "+txtNAE.getText()+" existe déja !!");
        }
        else{
            lMessage.setText("Veuillez remplir tous les champs .");
        }
    }//GEN-LAST:event_addAEActionPerformed
